str = input('Введите строку(5 и более знаков): ')
print('Третий символ: ', str[2])
print('Предпоследний символ: ', str[-2])
print('Первые пять символов: ', str[0:5])
print('Строка без 2-х последних символов: ', str[0:(len(str)-2)])
print('Символы с четными индексами: ', str[::2])
print('Символы с нечетными индексами: ', str[1::2])
print('Символы в обратном порядке: ', str[::-1])
print('Символы в обратном порядке, через один: ', str[::-2])
print('Длина строки: ', len(str), ' символов')

